class LuasSegitiga {
    public static void main(String[] args) {
        float alas = 20; // --> pembuatan variabel alas yang berisi 20 (type data float)
        float tinggi = 25; // --> pembuatan variabel tinggi yang berisi 25 (type data float)
        float luasSegitiga = (alas * tinggi)/2; // --> pembuatan variabel luas segitiga untuk melakukan perhitungan luas segitiga (type data float)
        System.out.println("luas Segitiga adalah " + luasSegitiga); // --> mencetak hasil luas segitiga
    }
}
